                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="criteria-for-account-based-selling"></span>Criteria for
                                                    Account-Based Selling<span class="ez-toc-section-end"></span></h2>



                                                <p>Account-based selling is a targeted approach to selling in which a
                                                    company or sales team focuses on a specific set of accounts rather
                                                    than
                                                    trying to sell to anyone and everyone. This approach can be very
                                                    effective, but it’s not right for every situation. Here are some
                                                    criteria to help you decide if account-based selling is right for
                                                    your
                                                    business.</p>



                                                <h3 class="wp-block-heading">You Have a Small Number of Target Accounts
                                                </h3>



                                                <p>Account-based selling is most effective when you have a small number
                                                    of
                                                    target accounts that you are trying to sell to. If you have a large
                                                    number of potential customers, it may be more difficult to focus
                                                    your
                                                    efforts on a specific group of accounts.</p>



                                                <h3 class="wp-block-heading">Your Target Accounts are High-Value
                                                    Accounts
                                                </h3>



                                                <p>Account-based selling is also more effective when your target
                                                    accounts
                                                    are high-value accounts. This means that they have the potential to
                                                    generate a significant amount of <a
                                                        href="https://dealhub.io/revenue-amplification/revenue-optimization/"
                                                        target="_blank" rel="noreferrer noopener">revenue</a> for your
                                                    business. If you’re selling to smaller businesses or individual
                                                    consumers, account-based selling may not be the best approach.</p>



                                                <h3 class="wp-block-heading">You Have a Dedicated Sales Team</h3>



                                                <p>Account-based selling requires a dedicated sales team that is focused
                                                    on
                                                    selling to your target accounts. This team should have in-depth
                                                    knowledge of your target accounts and their specific needs. If you
                                                    don’t
                                                    have a dedicated sales team, it may be difficult to implement an
                                                    account-based selling strategy.</p>



                                                <h3 class="wp-block-heading">You Have the Resources to Support an
                                                    Account-Based Selling Strategy</h3>



                                                <p>Account-based selling requires a significant investment of time and
                                                    resources. If you don’t have the internal resources to support an
                                                    account-based selling strategy, it may not be the right approach for
                                                    your business.</p>



                                                <h3 class="wp-block-heading">You’re Selling to Large Organizations</h3>



                                                <p>Account-based selling is typically most effective when you’re selling
                                                    to
                                                    large organizations. These organizations usually have the budget and
                                                    the
                                                    need for the products or services that you’re selling. If you’re
                                                    selling
                                                    to small businesses or individual consumers, account-based selling
                                                    may
                                                    not be the best approach.</p>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="implementing-account-based-sales-process"></span>Implementing
                                                    Account-Based Sales Process<span class="ez-toc-section-end"></span>
                                                </h2>



                                                <p>Sales teams have long been struggling to find an effective way to
                                                    sell to
                                                    large accounts. The traditional process of trying to engage multiple
                                                    decision-makers within a company is often ineffective and can lead
                                                    to a
                                                    lot of wasted time and effort. Account-based selling (ABS) is a new
                                                    sales strategy that is gaining popularity as it offers a more
                                                    targeted
                                                    and coordinated approach to selling to large accounts.</p>



                                                <p>The benefits of Account-based selling include:</p>



                                                <ul>
                                                    <li>Increased focus on key accounts</li>



                                                    <li>More efficient use of resources</li>



                                                    <li>Better coordination between departments</li>



                                                    <li>Improved win rates</li>



                                                    <li>Reduced customer acquisition cost</li>



                                                    <li>Increased <a
                                                            href="https://dealhub.io/glossary/average-deal-size/"
                                                            target="_blank" rel="noreferrer noopener">average deal
                                                            size</a>
                                                    </li>
                                                </ul>



                                                <p>However, there are also some challenges that need to be considered
                                                    when
                                                    implementing an ABS selling strategy. These include:</p>



                                                <ul>
                                                    <li>Ensuring that the sales team is aligned with other departments
                                                    </li>



                                                    <li>Ensuring that the sales team has the necessary skills and
                                                        knowledge
                                                    </li>



                                                    <li>Developing account plans</li>



                                                    <li>Setting account-based KPIs</li>
                                                </ul>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="account-based-sales-kpis"></span>Account-Based Sales
                                                    KPIs<span class="ez-toc-section-end"></span></h2>



                                                <p>If account-based selling is part of your sales strategy, you need to
                                                    track the right <a href="https://dealhub.io/blog/sales/sales-kpis/"
                                                        target="_blank" rel="noreferrer noopener">KPIs</a>. There are
                                                    several KPIs that measure the success of an account-based selling
                                                    strategy, but the three most important are:</p>



                                                <ul>
                                                    <li>Account Engagement: This measures how engaged your sales team is
                                                        with each account. Are they regularly reaching out and trying to
                                                        build relationships? Are they proactively trying to identify new
                                                        opportunities?</li>



                                                    <li>Account Conversion Rate: This measures the percentage of
                                                        accounts
                                                        that convert into paying customers.</li>



                                                    <li>Account Lifetime Value: This measures the average revenue
                                                        generated
                                                        from each account over the lifetime of the relationship.</li>
                                                </ul>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="structuring-your-sales-team-for-account-based-sales"></span>Structuring
                                                    Your Sales Team for Account-Based Sales<span
                                                        class="ez-toc-section-end"></span></h2>



                                                <p>There are a few ways to structure a sales team for an account-based
                                                    approach to selling. The most common approach is to create dedicated
                                                    account teams, with each team being responsible for pursuing and
                                                    winning
                                                    a specific account. Another option is to have a singular sales team
                                                    that
                                                    is responsible for all accounts, but this team is typically divided
                                                    into
                                                    smaller sub-teams, each with its own focus account.</p>



                                                <p>No matter which approach you take, there are a few key things to keep
                                                    in
                                                    mind when structuring your sales team for account-based selling.
                                                    First,
                                                    it’s important to make sure that each team member has a deep
                                                    understanding of the account they’re working on. This means ensuring
                                                    that they have access to all the necessary information and
                                                    resources.
                                                    Second, it’s important to create a clear process for pursuing and
                                                    winning accounts. This process should be well-defined and followed
                                                    by
                                                    all team members. Finally, it’s important to measure the success of
                                                    your
                                                    account-based selling efforts. This can be done by tracking key
                                                    metrics
                                                    such as win rate, deal size, and time to close.</p>



                                                <p>By following these tips, you can structure your sales team in a way
                                                    that
                                                    will allow them to successfully pursue and win key accounts.&nbsp;
                                                </p>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="how-to-use-content-in-account-based-selling"></span>How to
                                                    Use
                                                    Content in Account-Based Selling<span
                                                        class="ez-toc-section-end"></span>
                                                </h2>



                                                <p>As account-based selling has gained popularity in recent years, more
                                                    and
                                                    more companies are looking for ways to incorporate this methodology
                                                    into
                                                    their sales strategy. Content plays a key role in account-based
                                                    selling,
                                                    helping to identify and qualify potential customers, build
                                                    relationships, and ultimately drive revenue.</p>



                                                <p>When used effectively, content can be a powerful tool to <a
                                                        href="https://dealhub.io/blog/sales/win-more-deal-with-content/"
                                                        target="_blank" rel="noreferrer noopener">enrich the buyer’s
                                                        journey</a> and help you close more business. Here are a few
                                                    tips on
                                                    how to use content in Account-Based Selling:</p>



                                                <h3 class="wp-block-heading">1. Use content to identify and qualify
                                                    potential customers.</h3>



                                                <p>One of the first steps in any account-based selling strategy is
                                                    identifying prospective customers or target accounts. This can be a
                                                    challenge, especially if you are selling to a large and diverse
                                                    market.
                                                </p>



                                                <p>Content can be a great way to identify potential customers. Those who
                                                    engage with your content are likely good prospects. By creating
                                                    content
                                                    that is relevant to your target market, you can reach a larger
                                                    audience
                                                    and generate leads that are more likely to convert into sales.</p>



                                                <h3 class="wp-block-heading">2. Use content to build relationships with
                                                    potential customers.</h3>



                                                <p>Once you have identified potential customers, the next step is to
                                                    build
                                                    relationships with them. Content can be a great way to provide
                                                    valuable
                                                    information that potential customers can use.</p>



                                                <p>For example, if you sell software to businesses, you might create a
                                                    blog
                                                    post or white paper that discusses the benefits of your software and
                                                    how
                                                    it can help solve specific business problems. By providing valuable
                                                    information, you can start to build trust and relationships with
                                                    potential customers.</p>



                                                <h3 class="wp-block-heading">3. Use content to close more business.</h3>



                                                <p>Finally, once you have built relationships with potential customers,
                                                    you
                                                    can use content to close more business. This can be done by creating
                                                    custom content that is specific to the needs of your target
                                                    accounts.
                                                </p>



                                                <p>For example, if you have a target account that is interested in your
                                                    software, you might create a case study that discusses how your
                                                    software
                                                    has helped solve similar problems for other businesses and include
                                                    this
                                                    in your <a href="https://dealhub.io/glossary/sales-proposal/"
                                                        target="_blank" rel="noreferrer noopener">sales proposal</a>. By
                                                    providing relevant and targeted content in the right context, you
                                                    can
                                                    increase the likelihood of closing more business.</p>



