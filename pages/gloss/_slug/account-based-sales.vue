                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="what-is-account-based-selling"></span>What is Account-Based
                                                    Selling?<span class="ez-toc-section-end"></span></h2>



                                                <p>Account-based selling (ABS) is a strategy for sales teams to focus on
                                                    and
                                                    win business from specific high-value accounts. Account-based
                                                    selling
                                                    generally starts with identifying and targeting a specific list of
                                                    accounts that the sales team believes they can win. Once these
                                                    target
                                                    accounts have been identified, the sales team creates personalized
                                                    sales
                                                    strategies for each one. The goal of account-based selling is to
                                                    generate more revenue from fewer accounts by better aligning the <a
                                                        href="https://dealhub.io/glossary/sales-process/"
                                                        target="_blank" rel="noreferrer noopener">sales process</a> with
                                                    the needs and
                                                    buying behavior of large, complex organizations.</p>



                                                <p>Account-based selling can be contrasted with traditional lead-based
                                                    selling, which involves trying to sell to as many people as possible
                                                    without taking into account their specific needs or whether they are
                                                    likely to be interested in what you have to offer. Account-based
                                                    selling
                                                    is a more targeted and personalized approach that can be more
                                                    effective
                                                    for sales teams looking to win business from large organizations.
                                                    When
                                                    done well, it can result in higher win rates, larger deals, and
                                                    happier
                                                    customers.</p>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="synonyms"></span>Synonyms<span
                                                        class="ez-toc-section-end"></span></h2>



                                                <ul>
                                                    <li>ABM (Account-Based Marketing)&nbsp;</li>



                                                    <li>ABS (Account-Based Sales)</li>



                                                    <li>ABSD (Account-Based Sales Development)</li>
                                                </ul>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="the-account-based-selling-model"></span>The Account-Based
                                                    Selling Model<span class="ez-toc-section-end"></span></h2>



                                                <p>The ABS Model is a sales strategy that focuses on targeting
                                                    high-value
                                                    accounts and customizing the sales approach to fit the needs of each
                                                    account.</p>



                                                <p>Account-based selling has become a popular sales strategy in recent
                                                    years, as more and more companies are looking for ways to target and
                                                    win
                                                    over large, complex accounts. This sales model is based on the idea
                                                    that
                                                    each account is unique and requires a tailored approach. Account
                                                    managers work closely with other members of the sales team, as well
                                                    as
                                                    marketing and product teams, to develop a customized plan for each
                                                    target account.</p>



                                                <p>This sales strategy requires a high degree of coordination and
                                                    communication between all members of the sales team, as well as a
                                                    deep
                                                    understanding of the needs of each target account.</p>



                                                <h2 class="wp-block-heading"><span class="ez-toc-section"
                                                        id="implementing-an-account-based-sales-process-absp"></span>Implementing
                                                    an Account-Based Sales Process (ABSP)<span
                                                        class="ez-toc-section-end"></span></h2>



                                                <p>The Account-Based Sales Process (ABSP) is a <a
                                                        href="https://dealhub.io/glossary/sales-methodology/"
                                                        target="_blank" rel="noreferrer noopener">sales methodology</a>
                                                    that
                                                    focuses on selling to large accounts. The key difference between
                                                    ABSP
                                                    and traditional sales methods is that account managers take a
                                                    holistic
                                                    view of the customer, understanding all the key stakeholders within
                                                    an
                                                    organization and tailoring their pitch to each one.</p>



                                                <p>The goal of ABSP is to generate more revenue from large accounts, and
                                                    it’s a process that should be tailored to the unique needs of each
                                                    account. Here are five steps to implementing an account-based sales
                                                    process:</p>



                                                <ol>
                                                    <li>Define your <a
                                                            href="https://dealhub.io/glossary/ideal-customer-profile/"
                                                            target="_blank" rel="noreferrer noopener">ideal customer
                                                            profile</a>: In order to target the right accounts, you need
                                                        to
                                                        first define your ideal customer. Consider factors such as
                                                        company
                                                        size, industry, and location.</li>



                                                    <li>Research your target accounts: Once you know who your ideal
                                                        customer
                                                        is, it’s time to research specific companies that fit that
                                                        profile.
                                                        Learn as much as you can about the decision-makers within each
                                                        organization.</li>



                                                    <li>Create targeted account plans: Now that you know who you’re
                                                        selling
                                                        to and what their needs are, you can create targeted account
                                                        plans.
                                                        This involves mapping out the decision-making process within
                                                        each
                                                        organization and creating a unique sales strategy for each one.
                                                    </li>



                                                    <li>Implement dedicated account teams: To be successful with ABSP,
                                                        you
                                                        need to have dedicated account teams that are responsible for
                                                        managing relationships with large accounts. These teams should
                                                        be
                                                        composed of sales, marketing, and customer success
                                                        professionals.
                                                    </li>



                                                    <li>Measure success with account-based metrics: Finally, you need to
                                                        measure the success of your ABSP with account-based metrics.
                                                        This
                                                        includes tracking key performance indicators such as customer
                                                        lifetime value and customer churn rate.</li>
                                                </ol>



